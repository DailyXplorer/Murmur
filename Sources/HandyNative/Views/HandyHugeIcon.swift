struct HandyHugeIcon: View {
    let kind: HandyHugeIconKind
    let color: Color
    var size: CGFloat = 24
    var strokeWidth: CGFloat = 1.8
    var fill: Color?

    var body: some View {
        Canvas { context, canvasSize in
            let scale = min(canvasSize.width, canvasSize.height) / 24
            let side = 24 * scale
            let transform = CGAffineTransform(
                translationX: (canvasSize.width - side) / 2,
                y: (canvasSize.height - side) / 2
            )
            .scaledBy(x: scale, y: scale)
            let style = StrokeStyle(lineWidth: strokeWidth * scale, lineCap: .round, lineJoin: .round)

            for path in kind.paths {
                let transformedPath = path.applying(transform)
                if let fill {
                    context.fill(transformedPath, with: .color(fill))
                }
                context.stroke(transformedPath, with: .color(color), style: style)
            }
        }
        .frame(width: size, height: size)
        .accessibilityHidden(true)
    }
}

